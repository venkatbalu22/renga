        $(document).ready(function () {
            $('#submit').click(function () {
                var name = jQuery('#name').val();
                var phone = jQuery('#number').val();
                var email = jQuery('#email').val();
                var message = jQuery('#message').val();

                 console.log('name');
                if (name == "") {
                    toastr.warning('Enter Name');
                } else if (phone == "") {
                    toastr.warning('Enter Phone');
                } else if (email == "") {
                    toastr.warning('Enter Email');
                } else if (message == "") {
                    toastr.warning('Enter Message');
                } else {
                    var fd = new FormData();

                    fd.append('name', name);
                    fd.append('email', email);
                    fd.append('phone', phone);
                    fd.append('message', message);

                    jQuery.ajax({
                        url: 'ajax/email.php',
                        method: 'post',
                        contentType: false,
                        processData: false,
                        data: fd,

                        success: function (response) {
                            var result = JSON.parse(response);

                            if (result.status == 'Success') {
                                toastr.success('Email sent', 'Success');
                            } else {
                                toastr.error('Error sending mail', 'Error');
                            }
                        }
                    })
                }
            });
        });
    </script>

